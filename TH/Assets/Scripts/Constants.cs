	public Pattern GetPatternByName(string name) {
		Pattern pattern = null;
		foreach (var p in patterns) {
			if (p.name == name) {
				pattern = p;
				return pattern;
			}
		}
		return pattern;
	}

	private List<PatternFTR> GetFTR(List<PatternFTR> list, string[] stringList) {
		if (list.Count > 0)
			return list;

		PatternFTR flop = null;
		foreach (var item in stringList) {
			string[] items = item.Split ('\t');
			string arg0, arg1, arg2, arg3;
			arg0 = arg1 = arg2 = arg3 = "";
			
			if (items.Length > 0)
				arg0 = items[0];
			if (items.Length > 1)
				arg1 = items[1];
			if (items.Length > 2)
				arg2 = items[2];
			if (items.Length > 3)
				arg3 = items[3];
			
			if (arg0 == "OPPONENTS") {
				flop = new PatternFTR();
				flop.alt_patterns = new List<Pattern>();
				int opponentsCount = 0;
				Int32.TryParse(arg1, out opponentsCount);
				flop.enemyCount = opponentsCount;
				list.Add(flop);
			} else if (arg0 == "POSITION") {
				int position = 0;
				Int32.TryParse(arg1, out position);
				flop.position = position;
			} else if (arg0 == "RANGE") {
				double min = 0;
				double max = 0;
				Double.TryParse(arg1, out min);
				Double.TryParse(arg2, out max);
				flop.winPercentMin = min;
				flop.winPercentMin = max;
				
				flop.pattern = GetPatternByName(arg3);
				
				flop.pattern.percent = 100;
			} else if (arg0 == "ALT") {
				var altPattern = new Pattern();
				altPattern.name = arg1;
				double percent = 0;
				Double.TryParse(arg2, out percent);
				flop.pattern.percent -= percent;
				altPattern.percent = percent;
				flop.alt_patterns.Add (altPattern);
			}
		}
		//		"OPPONENTS	1",
		//		"POSITION	0",
		//		"RANGE	78	94	RAISE*",
		//		"ALT	CHECK/RAISE*	25",
		//		"ALT	CHECK/CALL*	35	",

		return list;
	}

