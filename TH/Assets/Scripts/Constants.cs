	public List<Pattern> GetPatterns() {
		if (patterns.Count > 0)
			return patterns;
		foreach (string item in c_str_pattern) {

			string[] items = item.Split ('\t');
			string arg0 = "";
			string arg1 = "";
			string arg2 = "";

			if (items.Length > 0)
				arg0 = items[0];
			if (items.Length > 1)
				arg1 = items[1];
			if (items.Length > 2)
				arg2 = items[2];

			if (arg0 == "PATTERN")
			{
				Pattern pattern = new Pattern();
				pattern.betSubRounds = new List<PatternBetRoundAndAction>();
				pattern.name = arg1;
				pattern.actionDefault = arg2;
				var prefActions = arg1.Replace("*", "").Split('/');
				if (prefActions.Length >= 2) {
					pattern.actionPriority2 = prefActions[0];
					pattern.actionPriority1 = prefActions[1];
					foreach(var c in prefActions[1]) {
						int maxBetAmount = 0;
						Int32.TryParse(c.ToString(), out maxBetAmount);
						if (maxBetAmount > 0) {
							pattern.betMaxCallOrRaise = maxBetAmount;
						} else {
							pattern.betMaxCallOrRaise = 1;
						}
					}
				}
				patterns.Add (pattern);
			} else {
				Pattern lastPattern = patterns[patterns.Count-1];
				PatternBetRoundAndAction betRound = new PatternBetRoundAndAction();

				var betChars = arg0.ToCharArray();
				betRound.name_action = arg1;
				double costBet = 0;
				Double.TryParse(betChars[0].ToString(), out costBet);
				betRound.costBet = costBet;
				double costBetTotal = 0;
				Double.TryParse(betChars[1].ToString(), out costBetTotal);
				betRound.costBetTotal = costBetTotal;
				lastPattern.betSubRounds.Add(betRound);
			}
		}
		return patterns;
	}

	public Pattern GetPatternByPreflopHand(string hand) {
		foreach (var item in preflops) {
